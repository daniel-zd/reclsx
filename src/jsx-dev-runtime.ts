import { Fragment, jsxDEV as _jsxDEV } from "react/jsx-dev-runtime";
import createClsxProps from "./create-clsx-props";

function jsxDEV(
  type: any,
  props: any,
  key: any,
  isStaticChildren: any,
  source: any,
  self: any
) {
  return _jsxDEV(
    type,
    createClsxProps(props),
    key,
    isStaticChildren,
    source,
    self
  );
}

export { Fragment, jsxDEV };
